object Calculator {

    fun sum(n1: Double, n2: Double): Double {
        return n1 + n2
    }

    fun subtract(n1: Double, n2: Double): Double {
        return n1 - n2
    }

    fun multiply(n1: Double, n2: Double): Double {
        return n1 * n2
    }

    fun divider(n1: Double, n2: Double): Double {
        return n1 / n2
    }

}